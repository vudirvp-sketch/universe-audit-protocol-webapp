// ============================================================
// Orientation context block builder
// ============================================================

function buildOrientationContextBlock(
  orientationContext: OrientationContext,
  block1RawMarkdown?: string,
): string {
  const { auditMode, authorProfileType, authorProfilePercentage, skeletonSummary } = orientationContext;

  // If we have at least the mode, use structured context
  if (auditMode !== null || authorProfileType !== null || skeletonSummary !== null) {
    const modeLabel = auditMode ?? 'не определён';
    const profileLabel = authorProfileType ?? 'не определён';
    const profilePct = authorProfilePercentage !== null ? ` ${authorProfilePercentage}%` : '';
    const skeletonBlock = skeletonSummary ?? '(скелет не извлечён)';

    return `КОНТЕКСТ ИЗ ПРЕДЫДУЩЕГО ЭТАПА:
Режим аудита: ${modeLabel}
Профиль автора: ${profileLabel}${profilePct}
Скелет концепта:
${skeletonBlock}`;
  }

  // Fallback: use raw Block 1 markdown
  return `КОНТЕКСТ ИЗ ПРЕДЫДУЩЕГО ЭТАПА (сырой текст):
${block1RawMarkdown ?? '(контекст недоступен)'}`;
}

